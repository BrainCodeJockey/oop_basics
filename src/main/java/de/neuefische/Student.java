package de.neuefische;

public class Student {
    public String name;
    public int age;
    public boolean isWebDev;
    public String gender;

    public Student(String name, int age, boolean isWebDev, String gender)  {
        this.name = name;
        this.age = age;
        this.isWebDev = isWebDev;
        this.gender = gender;
    }
}
